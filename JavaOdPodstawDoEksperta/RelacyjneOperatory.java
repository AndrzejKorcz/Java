public class RelacyjneOperatory {
    
        public static void main(String[] args) {
        /*
            OPERATOR RELACYJNY (PORÓWNANIA)
            
            == - porównanie
            != - porównanie czy są różne od siebie
            > - większe od
            < - mniejsze od
            >= - większe bądź równe
            <= - mniejsze bądź rowne
        */
        int a = 5,
            b = 7;
       
        System.out.println(a > b);
    }
    
}
